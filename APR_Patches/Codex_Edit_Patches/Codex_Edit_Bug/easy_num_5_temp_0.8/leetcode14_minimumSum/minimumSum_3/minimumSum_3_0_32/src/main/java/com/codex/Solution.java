public class CarryOn {
    public static void main(String[] args) {
        int i = 0;
        while (i < 10)
            System.out.print(i++);
        // Can you guess what's the output?

        for (int i = 0; i < 10; i++) {
            System.out.print(i);
        }

        // Can you guess what's the output?
    }
}
