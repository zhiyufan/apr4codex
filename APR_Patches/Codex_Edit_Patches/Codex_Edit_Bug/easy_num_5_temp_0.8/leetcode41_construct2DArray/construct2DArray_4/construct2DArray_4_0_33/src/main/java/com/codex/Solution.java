package com.codex;

import java.util.*;

public class Solution {

    public static void construct2DArray(int[] original, int m, int n) {
        
        
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                
                System.out.print(original[m][n]);
            }
        }
       
    }

   
}