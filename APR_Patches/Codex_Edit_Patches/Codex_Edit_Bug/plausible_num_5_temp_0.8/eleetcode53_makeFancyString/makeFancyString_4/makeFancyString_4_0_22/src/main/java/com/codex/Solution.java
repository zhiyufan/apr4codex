    public static String makeFancyString(String s) {
      String result = "";
      ArrayList<Character> s_c = new ArrayList<Character>();
      for (int i = 0; i < s.length(); i++) {
        s_c.add(s.charAt(i));
      }
      for (int i = 0; i < s_c.size() - 1; i++) {
        if (s_c.get(i) != s_c.get(i + 1)) {
          // we need to add the char
          result += s_c.get(i);
        } else {
          // we need to skip this char
          continue;
        }
      }
      result += s_c.get(s_c.size() - 1);
      return result;
    }

